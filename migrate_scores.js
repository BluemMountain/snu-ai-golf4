function parseCSV(csv) {
    const lines = csv.trim().split('\n');
    return lines.map(line => line.split(','));
}

async function migrateScores() {
    const data = parseCSV(CSV_DATA_STRING);
    // 첫 번째 행에서 회원 이름 목록 추출 (인덱스 3부터)
    const names = data[0].slice(3).map(n => n.trim());

    // 두 번째 행은 핸디캡 정보를 담고 있지만, 여기서는 라운드 기록(인덱스 2부터)만 마이그레이션
    const rows = data.slice(2);

    console.log(`총 ${rows.length}개의 라운드 기록을 마이그레이션합니다...`);

    for (const row of rows) {
        const round_count = parseInt(row[0]) || 0;
        const date = row[1];
        const venue = row[2];
        const scores_data = {};

        names.forEach((name, idx) => {
            if (!name) return;
            const score = row[idx + 3];
            if (score && score.trim() !== '' && score !== '-' && score !== '0') {
                scores_data[name] = score.trim();
            }
        });

        // 빈 데이터는 건너뜀 (예: 라운드 6)
        if (Object.keys(scores_data).length === 0 && !date) continue;

        const { error } = await supabase
            .from('scores')
            .upsert([{
                round_count,
                date,
                venue,
                scores_data
            }], { onConflict: 'round_count' }); // 중복 방지를 위해 upsert 사용 (unique index 필요)

        if (error) {
            console.error(`라운드 ${round_count} 저장 실패:`, error.message);
        } else {
            console.log(`라운드 ${round_count} (${date}, ${venue}) 저장 완료.`);
        }
    }

    console.log('마이그레이션 종료.');
}

migrateScores();
